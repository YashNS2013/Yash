str2 = "Akshobhya Shantanu Vivikta Aditya Yash Nakshatra Kiran Amartya"

# count small letter 'a'
count = 0

for ch in str2:
    if ch == 'a':
        count = count + 1

print("The number of small letter 'a' in the string is:", count)


